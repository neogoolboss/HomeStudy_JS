/**
 * Naming Conventions
 * 
 * 1) 일반적으로 영어를 사용하며 문자와 숫자를 모두 사용할 수 있다.
 * 2) 특수기호는 언더스코어('_')와 달러('$')fmf 사용할 수 있다.
 * 3) 숫자로 이름을 시작할 수 없다.
 * 4) 키워드는 변수명으로 사용할 수 없다.
 *    ex) var const = 'var';
 */

let javaScript = '자바스크립트';
var $ing = '노래';
const _game = '리그오브레전드';

console.log(javaScript);
console.log($ing);
console.log(_game);

